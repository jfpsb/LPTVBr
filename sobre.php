		<p>A iniciativa de legendar vídeos da banda Linkin Park começou em
			2010, em uma comunidade do Orkut. Naquela época eu, Felipe, legendava
			os vídeos com a ajuda de vários membros da comunidade, pois não tinha
			conhecimento suficiente em inglês. Com o passar do tempo, acabei
			aprendendo o suficiente para tocar o canal sozinho.</p>
		<p>
			Na "primeira versão" do canal, os vídeos eram postados em uma conta
			pessoal minha no Youtube, que logo foi deletada por conta de direitos
			autorais. Na "segunda versão", a mais "famosa" até hoje, criei um
			perfil específico no Youtube com o nome "LinkinParkTVBr". Por quase
			dois anos o canal cresceu bastante chegando à marca de dois milhões
			de visualizações, quase dez mil inscritos e quase trezentos vídeos
			postados. Entretanto, de um dia para o outro, uma empresa detentora
			de direitos sobre o concerto da banda Linkin Park em Monterrey, no
			México, começou a dar
			<dfn>flag</dfn>
			<a href="#rodape1"><sup>1</sup></a> em vídeos desse concerto e quando
			um canal leva três flags no Youtube, ele é excluído.
		</p>
		<p>Depois disso, pensei em parar completamente com o canal, pois
			estava desmotivado, mas logo depois decidi retornar e nessa "terceira
			versão" tomo muito mais cuidado com direitos autorais. O conteúdo
			pode ficar limitado por conta disso, mas é necessário. Agradeço a
			todos que acompanham o canal. Muito obrigado e até o próximo vídeo.</p>
